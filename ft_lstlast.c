#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	int		i;
	t_list	*current;

	i = 0;
	current = lst;
	if (current == NULL)
		return (NULL);
	while (current->next != NULL)
	{
		current = current->next;
		i++;
	}
	return (current);
}
